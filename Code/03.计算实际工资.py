'''
某职工每天的工资是P元，本月他工作了D天，本月应缴工会会费为月工资的0.5%、
房租水电费为月工资的1.5%、扣除公积金为月工资的3.5%及医药费27.83元。
输入日工资和月工作天数计算出职工本月实得工资。
'''

P=float(input('请输入日工资：'))
D=int(input('请输入工作天数：'))
result=float((0.945*P*D)-27.83)
print('实际工资为：%.1f元' %result)